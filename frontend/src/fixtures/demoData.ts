const buildGraph = (processId: string, steps: StepInsight[]): ProcessGraph => {
  const span = steps.length > 1 ? 76 / (steps.length - 1) : 0;
  const nodes = steps.map((step, index) => ({
    id: step.id,
    label: step.name,
    tier: step.nodeType,
    x: 12 + index * span,
    y: step.nodeType === "decision" ? 18 : index % 2 === 0 ? 30 : 26,
    emphasis: Math.min(100, step.contextSwitches * 9 + step.copyPasteSignals * 7),
  }));

  const edges = steps.slice(0, -1).map((step, index) => ({
    source: step.id,
    target: steps[index + 1].id,
    weight: Math.max(24, 90 - index * 7),
    label: index === 2 && processId === "communication" ? "copy loop" : undefined,
  }));

  if (processId === "communication") {
    edges.push({
      source: "comm-step-5",
      target: "comm-step-3",
      weight: 32,
      label: "clarify",
    });
  }

  return {
    processId,
    nodes,
    edges,
  };
};

